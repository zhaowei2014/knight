package com.zw.knight.pojo;

/**
 * 单链表
 *
 * @author zw
 * @date 2020/7/14
 */
public class LinkNode {
    private int num;
    private LinkNode next;

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public LinkNode getNext() {
        return next;
    }

    public void setNext(LinkNode next) {
        this.next = next;
    }
}
